#include "main.h"
#include <ctype.h>

/**
* _isupper - checks for uppercase character
* @c: character is accepted
*
* Return: Always 1 if character uppercase but 0 if otherwise
*/
int _isupper(int c);
{
	int checker = isupper(c);

	if  (checker = 1)
	{
		_putchar(c);
		_putchar(':');
		_putchar(' ');
		_putchar(checker);
	}
	else
	{
		_putchar(c);
		_putchar(':');
		_putchar(' ');
		_putchar(checker);
	}
}
